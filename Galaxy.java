class Galaxy{
	static String typesOfGalaxies;
	static String temperature;
	static boolean spaceEverEnd;
		public static void setTypesOfGalaxies(String types){
			typesOfGalaxies=types;
		}
		public static String getTypesOfGalaxies(){
			return typesOfGalaxies;
		}
		public static void setTemperature(String temp){
			temperature=temp;
		}
		public static String getTemperature(){
			return temperature;
		}
		public static void setSpaceEverEnd(boolean spaceEver){
			spaceEverEnd=spaceEver;
		}
		public static boolean getSpaceEverEnd(){
			return spaceEverEnd;
		}
}