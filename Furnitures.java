class Furnitures{
	static String name;
	static String brandName;
	static int price;
	static String color;
	static String perfectFor;
		public static void setName(String nam){
			name=nam;
		}
		public static String getName(){
			return name;
		}
		public static void setBrandName(String brandNam){
			brandName=brandNam;
		}
		public static String getBrandName(){
			return brandName;
		}
		public static void setPrice(int amount){
			price=amount;
		} 
		public static int getPrice(){
			return price;
		}
		public static void setColor(String colour){
			color=colour;
		}
		public static String getColor(){
			return color;
		}
		public static void setPerfectFor(String perfect){
			perfectFor=perfect;
		}
		public static String getPerfectFor(){
		 return perfectFor;
		}
}