class Mannu{
	static String name;
	static String color;
	static String properties;
		public static void setName(String nam){
			name=nam;
		}
		public static String getName(){
			return name;
		}
		public static void setColor(String colour){
			color=colour;
		}
		public static String getColor(){
			return color;
		}
		public static void setProperties(String property){
			properties=property;
		}
		public static String getProperties(){
			return properties;
		}
}