class Vesseles{
	static String name;
	static int price;
	static String features;
		public static void setName(String nam){
			name=nam;
		}
		public static String getName(){
			return name;
		}
		public static void setPrice(int amount){
			price=amount;
		}
		public static int getPrice(){
			return price;
		}
		public static void setFeatures(String feature){
			features =feature;
		}
		public static String getFeatures(){
			return features;
		}
}